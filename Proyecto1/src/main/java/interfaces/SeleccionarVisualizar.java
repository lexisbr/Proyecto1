    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jComboBox1.getSelectedItem().equals("Tienda")) {
            VisualizarTienda tienda = new VisualizarTienda();
            tienda.setVisible(true);
            this.dispose();
        }else if(jComboBox1.getSelectedItem().equals("Tiempo entre tiendas")){
            VisualizarTiempo tiempotienda = new VisualizarTiempo();
            tiempotienda.setVisible(true);
            this.dispose();
        }else if(jComboBox1.getSelectedItem().equals("Producto")){
            VisualizarProducto a = new VisualizarProducto();
            a.setVisible(true);
            this.dispose();
        }else if(jComboBox1.getSelectedItem().equals("Empleado")){
            VisualizarEmpleado a = new VisualizarEmpleado();
            a.setVisible(true);
            this.dispose();
        }else if(jComboBox1.getSelectedItem().equals("Cliente")){
            VisualizarCliente a = new VisualizarCliente();
            a.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

